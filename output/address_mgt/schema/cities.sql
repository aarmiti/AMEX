CREATE SCHEMA IF NOT EXISTS `ADDRESS_MGT` DEFAULT CHARACTER SET utf8;

USE `ADDRESS_MGT`;

DROP TABLE IF EXISTS `ADDRESS_MGT`.`CITIES`;

CREATE TABLE `ADDRESS_MGT`.`CITIES` (
  `ID` BIGINT UNSIGNED NOT NULL AUTO_INCREMENT,

  -- Persistent columns --
  `NAME` VARCHAR(10) NOT NULL,

  -- Relational columns --
  `COUNTRY_ID` BIGINT UNSIGNED,

  -- Constraint --
  CONSTRAINT `FK_COUNTRY_ID` FOREIGN KEY (`COUNTRY_ID`) REFERENCES `ADDRESS_MGT`.`COUNTRIES` (`ID`) ON DELETE NO ACTION ON UPDATE NO ACTION,
  PRIMARY KEY (`ID`) USING BTREE
) ENGINE=InnoDB AUTO_INCREMENT=1 DEFAULT CHARSET=utf8 COLLATE=utf8_unicode_ci;
