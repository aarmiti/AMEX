CREATE SCHEMA IF NOT EXISTS `ADDRESS_MGT` DEFAULT CHARACTER SET utf8;

USE `ADDRESS_MGT`;

DROP TABLE IF EXISTS `ADDRESS_MGT`.`JUNCTION_TASKS_WITH_TAGS`;

CREATE TABLE `ADDRESS_MGT`.`JUNCTION_TASKS_WITH_TAGS` (
  `ID` BIGINT UNSIGNED NOT NULL AUTO_INCREMENT,

  -- Relationship columns --
  `TASK_ID` BIGINT UNSIGNED,
  `TAG_ID` BIGINT UNSIGNED,

  -- Constraint --
  CONSTRAINT `FK_TASK_ID` FOREIGN KEY (`TASK_ID`) REFERENCES `ADDRESS_MGT`.`TASKS` (`ID`) ON DELETE NO ACTION ON UPDATE NO ACTION,
  CONSTRAINT `FK_TAG_ID` FOREIGN KEY (`TAG_ID`) REFERENCES `ADDRESS_MGT`.`TAGS` (`ID`) ON DELETE NO ACTION ON UPDATE NO ACTION,
  PRIMARY KEY (`ID`) USING BTREE
) ENGINE=InnoDB AUTO_INCREMENT=1 DEFAULT CHARSET=utf8 COLLATE=utf8_unicode_ci;
